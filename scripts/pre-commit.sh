#!/bin/bash

# 1. Выводим сообщение для пользователя
echo "🔄 Запуск автоматической проверки курса перед коммитом..."

# 2. Запускаем нашу команду валидации (точка означает "в текущей папке")
course-cli validate .

# 3. Проверяем код возврата последней выполненной команды
if [ $? -ne 0 ]; then
# 4. Если код не равен 0 (была ошибка), прерываем коммит
    echo "❌ Ошибка: В курсе найдены проблемы! Исправьте их перед сохранением."
    exit 1
fi

# 5. Если код равен 0 (успех), разрешаем коммит
echo "✅ Курс прошел проверку. Выполняю коммит..."
exit 0